public class CompanyCostumer extends Costumer{
    private String nameOfCompany;
    private String numberOfStaff;
    private String dateOfPublic;

    public String getDateOfPublic() {
        return dateOfPublic;
    }

    public void setDateOfPublic(String dateOfPublic) {
        this.dateOfPublic = dateOfPublic;
    }

    public String getNumberOfStaff() {
        return numberOfStaff;
    }

    public void setNumberOfStaff(String numberOfStaff) {
        this.numberOfStaff = numberOfStaff;
    }

    public String getNameOfCompany() {
        return nameOfCompany;
    }

    public void setNameOfCompany(String nameOfCompany) {
        this.nameOfCompany = nameOfCompany;
    }
}
